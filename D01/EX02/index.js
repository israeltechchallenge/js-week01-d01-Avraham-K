function isBiggerThan(Num1) {
  if (Num1 > 4) {
    console.log(`The number ${Num1} is bigger than 4`)
  } else if (Num1 < 4) {
    console.log(`The number ${Num1} is less than 4`)
  } else {  
    console.log(`The number ${Num1} is equal to 4`)
  }
}

// Do not remove or change this line, or the tests won't work
export { isBiggerThan };
